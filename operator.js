//operator javascript are used to perform different types of matematika dan logika komputasi
//1. operator penugasan =
//2. operator penambahan +
//3. operator perkalian *
//4. operator perbandingan >

//Operator aritmatika js
let a = 3;
let b = (100 + 50) * a;
console.log(b);

//operator penugasan js
let c = 10;
c -= 5;
console.log(c);

//operator comparison
let text1 = "A";
let text2 = "B";
let result = text1 == text2;
console.log(result);

//Increment
let d = 10;
d++;
console.log(d);

//eksponen
let e = 10;
let f = e ** 2;
console.log(f);

//deecrement
let g = 100;
g--;
console.log(g);

//Javascript Assignment Operators
//1. Bitwise Assignment Operators

//AND
let h = 100;
h &= 5;
console.log(h);
//OR
let i = 100;
i |= 5;
console.log(i);
